package pacman.hunter;

/**
 * A class represents different hunter types
 */
public enum HunterType {
    /**
     * A hunter can eat ghosts temporarily.
     */
    HUNGRY,

    /**
     * A hunter can phase through ghosts temporarily.
     */
    PHASEY,

    /**
     * basic phil, has no special ability.
     */
    PHIL,

    /**
     * A hunter can run temporarily.
     */
    SPEEDY;

}
