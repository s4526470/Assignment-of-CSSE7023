package pacman.ghost;

/**
 * A class to represents the ghost type
 */
public enum GhostType {

    /**
     * Aggressive red ghost.
     */
    BLINKY,

    /**
     * Scared orange ghost.
     */
    CLYDE,

    /**
     * Following blue ghost.
     */
    INKY,

    /**
     * Ambushing pink ghost.
     */
    PINKY;
}
